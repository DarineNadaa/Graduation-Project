-- =========================================================
-- ATTENSE PostgreSQL Schema
-- =========================================================
-- Canonical PostgreSQL schema for ATTENSE runtime data.
-- This version uses PostgreSQL syntax and adds a first-pass role/permission
-- model so access rules can live alongside the data model.
-- =========================================================

CREATE SCHEMA IF NOT EXISTS attense;
SET search_path TO attense;

CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- =========================================================
-- Roles and permissions
-- =========================================================
DO $$
BEGIN
    CREATE TYPE user_role AS ENUM (
        'ciso',
        'soc_manager',
        'soc_l1',
        'soc_l2',
        'red_team'
    );
EXCEPTION
    WHEN duplicate_object THEN NULL;
END $$;

CREATE TABLE IF NOT EXISTS "RolePermission" (
    role_name      user_role   NOT NULL,
    permission_key text        NOT NULL,
    description    text        NOT NULL,
    enabled        boolean     NOT NULL DEFAULT true,
    PRIMARY KEY (role_name, permission_key)
);

